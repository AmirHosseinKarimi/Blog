    }

    /**
     * Get subscribers count in last 7 days.
     *
     * @return int
     */
    public static function getNewSubscribersCount()
    {
        return User::where('role', 'subscriber')
                   ->whereDate('created_at', '>=', Carbon::now()->subDays(6))
                   ->count();
    }
